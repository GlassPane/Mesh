package dev.upcraft.mesh.mixin.debug.client.session;

import dev.upcraft.mesh.util.coremods.session.ClientRunArgsAccessor;
import net.minecraft.client.RunArgs;
import net.minecraft.client.util.Session;
import org.spongepowered.asm.mixin.Final;
import org.spongepowered.asm.mixin.Mixin;
import org.spongepowered.asm.mixin.Mutable;
import org.spongepowered.asm.mixin.Shadow;

@Mixin(RunArgs.Network.class)
public abstract class RunArgsNetworkAccessor implements ClientRunArgsAccessor {

    @Mutable
    @Shadow @Final public Session session;

    @Override
    public void mesh_dev_updateSession(Session session) {
        this.session = session;
    }
}
