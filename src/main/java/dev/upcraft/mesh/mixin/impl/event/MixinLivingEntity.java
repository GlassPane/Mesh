package dev.upcraft.mesh.mixin.impl.event;

import dev.upcraft.mesh.api.event.entity.MeshEntityEvents;
import net.minecraft.entity.LivingEntity;
import net.minecraft.entity.damage.DamageSource;
import org.spongepowered.asm.mixin.Mixin;
import org.spongepowered.asm.mixin.injection.At;
import org.spongepowered.asm.mixin.injection.Inject;
import org.spongepowered.asm.mixin.injection.callback.CallbackInfoReturnable;

@Mixin(LivingEntity.class)
public abstract class MixinLivingEntity {

    @Inject(method = "damage", at = @At(value = "INVOKE", target = "Lnet/minecraft/entity/LivingEntity;onDeath(Lnet/minecraft/entity/damage/DamageSource;)V", shift = At.Shift.AFTER))
    private void onDeath(DamageSource source, float amount, CallbackInfoReturnable<Boolean> cir) {
        MeshEntityEvents.LIVING_ENTITY_DEATH.invoker().consume((LivingEntity) (Object) this, source, amount);
    }
}
